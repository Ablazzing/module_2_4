package org.javaacademy.homework.homework2.ex1;

public class Luggage {
    private String number;
    private int weight;

    public Luggage(String number, int weight) {
        this.number = number;
        this.weight = weight;
    }

    public String getNumber() {
        return number;
    }

    public int getWeight() {
        return weight;
    }
}
